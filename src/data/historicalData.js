export const ERA_RANGES = [
  { id: 'ancient',        label: 'Ancient',        start: -3000, end: -500  },
  { id: 'classical',      label: 'Classical',       start: -500,  end: 500   },
  { id: 'early_medieval', label: 'Early Medieval',  start: 500,   end: 1000  },
  { id: 'high_medieval',  label: 'High Medieval',   start: 1000,  end: 1300  },
  { id: 'late_medieval',  label: 'Late Medieval',   start: 1300,  end: 1500  },
  { id: 'early_modern',   label: 'Early Modern',    start: 1500,  end: 1800  },
  { id: 'modern',         label: 'Modern',          start: 1800,  end: 2026  },
];

export function getEraForYear(year) {
  return (
    ERA_RANGES.find(e => year >= e.start && year <= e.end) ||
    ERA_RANGES[ERA_RANGES.length - 1]
  );
}

export function formatYear(year) {
  if (year < 0) return `${Math.abs(year)} BC`;
  if (year === 0) return '1 AD';
  return `${year} AD`;
}

